#include "libft.h"

void		ft_putnbr(int n)
{
	if (n < 0)
		write(1, "-", 1);
	if (ft_abs(n / 10))
		ft_putnbr(ft_abs(n / 10));
	ft_putchar(ft_abs(n % 10) + '0');
}
